public class Operadores2 {
    public static void main(String[] args) {
        int numeroUm = 1;
        int numeroDois = 2;

        boolean simOuNao = numeroUm == numeroDois;

        System.out.println("Numero 1 é igual á numero 2 ? " + simOuNao);

        simOuNao = numeroUm > numeroDois;

        System.out.println("Numero 1 é maior que numero 2 ? " + simOuNao);

        simOuNao = numeroUm < numeroDois;

        System.out.println("Numero 1 é maior que numero 2 ? " + simOuNao);
    }
}
